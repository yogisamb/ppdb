        // small
        var element = document.getElementById("filebaru");
        var newElement = '<p class="removeablegy" style="color: red; margin-left: 2px;"> Isi form nama sekolah jika pilihan sekolah tidak ada di dalam pilihan </p>'
        element.insertAdjacentHTML('afterend', newElement)
        // document.body.insertAdjacentHTML('afterbegin', '<div id="myID">...</div>');
      } else {
        document.getElementById('manual').type = 'hidden';
        $(".removeablegy").remove();
      }
    });
  });
</script>
